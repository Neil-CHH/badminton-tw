def is_promoted(category, group, rank):
    """排名賽:乙組單打前 4、乙組雙打前 3 晉升甲組。"""
    if category != "排名賽" or not group.startswith("乙組"):
        return False
    if group.endswith("單打"):
        return rank <= 4
    if group.endswith("雙打"):
        return rank <= 3
    return False


def build_entry(category, group, rank, unit, members):
    e = {"group": group, "rank": rank, "members": members, "source": "pdf"}
    if isinstance(unit, (tuple, list)):
        uniq = list(dict.fromkeys(unit))
        if len(uniq) == 1:
            e["unit"] = uniq[0]
        else:
            e["unit"] = "／".join(uniq)        # 顯示用
            e["memberUnits"] = list(unit)       # 各選手歸屬(供索引)
    else:
        e["unit"] = unit
    if is_promoted(category, group, rank):
        e["promoted"] = True
    return e


def apply_one(openid, spec):
    path = TOURN_DIR / f"{openid}.json"
    rec = json.loads(path.read_text(encoding="utf-8"))
    category = spec["category"]
    events = spec["events"]
    pdf_groups = [g for g, _ in events]

    standings = []
    for group, entries in events:
        for rank, unit, members in entries:
            standings.append(build_entry(category, group, rank, unit, members))
    # 合併:本次匯入的組別覆蓋舊資料,其他組別(若有)保留
    kept = [s for s in rec.get("standings", []) if s.get("group") not in pdf_groups]
    rec["standings"] = sorted(kept + standings, key=lambda s: (s["group"], s["rank"]))

    # groups:本次組別補齊(保留既有其他組別)
    existing_names = {g.get("name") for g in rec.get("groups", [])}
    for group in pdf_groups:
        if group not in existing_names:
            rec.setdefault("groups", []).append({
                "id": "", "name": group,
                "tags": parse_group_tags(group, None), "drawUrl": None})

    rec["category"] = category
    rec["promotion"] = spec.get("promotion")
    path.write_text(json.dumps(rec, ensure_ascii=False, separators=(",", ":")),
                    encoding="utf-8")
    promoted = sum(1 for s in standings if s.get("promoted"))
    print(f"  {openid} {rec['name']}: 名次 {len(standings)} 筆"
          f"(晉升 {promoted})、組別 {len(pdf_groups)}、category={category}")


