def is_promoted(category, group, rank):
    """排名賽:乙組單打前 4、乙組雙打前 3 晉升甲組。"""
    if category != "排名賽" or not group.startswith("乙組"):
        return False
    if group.endswith("單打"):
        return rank <= 4
    if group.endswith("雙打"):
        return rank <= 3
    return False


